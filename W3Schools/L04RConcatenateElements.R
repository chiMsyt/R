# R Concatenate Elements

# Concatenate Elements
# You can also concatenate, or join, two or more elements, by using paste()

# Example
text <- "awesome"

paste("R is", text) # output: "R is awesome"

text1 <- "R is"
text2 <- "awesome"

paste(text1, text2) # output: "R is awesome"

# For numbers, the + character works as a mathematical operator

num1 <- 5
num2 <- 10
num1 + num2 # output: 15
